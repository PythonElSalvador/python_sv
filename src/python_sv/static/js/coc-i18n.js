  var esCache = {};
  var currentLang = document.documentElement.lang === 'en' ? 'en' : 'es';
  var btn = document.getElementById('langToggle');
  var hasOwn = Object.prototype.hasOwnProperty;

  function forEachCoCTranslation(callback) {
    document.querySelectorAll('[data-i18n]').forEach(function(el) {
      var key = el.getAttribute('data-i18n');
      if (hasOwn.call(en, key)) callback(el, key);
    });
  }

  function applyEN() {
    forEachCoCTranslation(function(el, key) {
      if (!hasOwn.call(esCache, key)) esCache[key] = el.innerHTML;
      el.innerHTML = en[key];
    });
  }

  function applyES() {
    forEachCoCTranslation(function(el, key) {
      if (hasOwn.call(esCache, key)) el.innerHTML = esCache[key];
    });
  }

  if (btn && localStorage.getItem('pysv-lang') === 'en') {
    applyEN();
    currentLang = 'en';
    btn.textContent = 'ES';
    document.documentElement.lang = 'en';
  }

  if (btn) {
    btn.addEventListener('click', function() {
      if (currentLang === 'es') {
        applyEN();
        currentLang = 'en';
      } else {
        applyES();
        currentLang = 'es';
      }
    });
  }

  var tocLinks = document.querySelectorAll('.pysv-coc-toc a');
  var sections = [];
  tocLinks.forEach(function(link) {
    var id = link.getAttribute('href').slice(1);
    var el = document.getElementById(id);
    if (el) sections.push({ id: id, el: el, link: link });
  });

  function updateToc() {
    var scrollY = window.scrollY + 120;
    var current = sections[0];
    for (var i = 0; i < sections.length; i++) {
      if (sections[i].el.offsetTop <= scrollY) current = sections[i];
    }
    tocLinks.forEach(function(l) { l.classList.remove('active'); });
    if (current) current.link.classList.add('active');
  }

  window.addEventListener('scroll', updateToc, { passive: true });
  updateToc();
